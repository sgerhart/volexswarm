    def show_current_ml_models(self):
        """Show current ML models and their status."""
        self.print_section("Current ML Models Status")
        
        try:
            # Get model performance
            response = requests.get(f"{self.signal_agent_url}/models/performance", timeout=10)
            if response.status_code == 200:
                performance_data = response.json()
                
                print(f"ü§ñ Total Models: {performance_data.get('total_models', 0)}")
                print(f"üîÑ Autonomous Mode: {'Enabled' if performance_data.get('autonomous_mode', False) else 'Disabled'}")
                print()
                
                models = performance_data.get('models', {})
                if models:
                    for symbol, model_info in models.items():
                        print(f"üìä {symbol}:")
                        print(f"   Model Trained: {'‚úÖ' if model_info.get('model_trained', False) else '‚ùå'}")
                        print(f"   Signal Count: {model_info.get('signal_count', 0)}")
                        
                        insights = model_info.get('insights', {})
                        if isinstance(insights, dict):
                            print(f"   Trend: {insights.get('trend', 'Unknown')}")
                            print(f"   Recommendation: {insights.get('recommendation', 'No recommendation')}")
                        print()
                else:
                    print("üü° No trained models found")
                    print("üí° Models are trained automatically when sufficient data is available")
                    
            else:
                print(f"‚ùå Error getting model performance: HTTP {response.status_code}")
                
        except Exception as e:
            print(f"‚ùå Error: {e}")
    
    def demonstrate_ai_decision_making(self):
        """Demonstrate AI decision making in action."""
        self.print_section("AI Decision Making Demonstration")
        
        print("üß† Let's see the AI making autonomous decisions:")
        print()
        
        symbols = ["BTCUSD", "ETHUSD"]
        
        for symbol in symbols:
            print(f"ü§ñ AI Analysis for {symbol}:")
            
            try:
                # Get autonomous decision
                response = requests.post(
                    f"{self.signal_agent_url}/autonomous/decide",
                    params={"symbol": symbol, "current_balance": 10000},
                    timeout=30
                )
                
                if response.status_code == 200:
                    data = response.json()
                    
                    if "decision" in data:
                        decision = data["decision"]
                        signal = data.get("signal", {})
                        
                        print(f"  üéØ Decision: {decision.get('action', 'hold').upper()}")
                        print(f"  üìä Confidence: {decision.get('confidence', 0):.1%}")
                        print(f"  üí≠ Reason: {decision.get('reason', 'No reason provided')}")
                        print(f"  üí∞ Position Size: ${decision.get('position_size', 0):,.2f}")
                        print(f"  ‚ö†Ô∏è  Risk Level: {decision.get('risk_level', 'unknown')}")
                        
                        # Show technical signals
                        if "signal" in data and "technical_signals" in signal:
                            tech_signals = signal["technical_signals"]
                            if tech_signals:
                                print(f"  üìà Technical Signals:")
                                for signal_name, strength in tech_signals:
                                    print(f"     ‚Ä¢ {signal_name} (strength: {strength})")
                        
                        # Show ML prediction
                        if "signal" in data and "ml_confidence" in signal:
                            ml_conf = signal["ml_confidence"]
                            ml_pred = signal.get("ml_prediction", 0)
                            if ml_conf > 0:
                                pred_text = "BUY" if ml_pred == 1 else "SELL" if ml_pred == -1 else "HOLD"
                                print(f"  ü§ñ ML Prediction: {pred_text} (confidence: {ml_conf:.1%})")
                        
                    else:
                        print(f"  üü° No decision data available")
                        
                else:
                    print(f"  ‚ùå Error: HTTP {response.status_code}")
                    
            except Exception as e:
                print(f"  ‚ùå Error: {e}")
            
            print()
    
